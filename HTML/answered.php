									<td class="answercell">
    									<div class="post-text"><p>Views doesn't load the complete <code>$node</code> object for performance reason. Instead, it build, or attempt to build, an optimized query to only load the fields configured in the Views.</p><p>When editing the views, you should have a <em>Fields</em> block. In this block, use its <em>Add fields</em> links to add the additional fields you want to use in your template. If you don't want to display these fields using Views itself, be sure to configure them to be excluded from display.</p>
